SDL_Texture *load_font(SDL_Renderer *renderer) {
  // Create an SDL_Surface with 32-bit RGBA pixel format
  SDL_Surface *surface =
      SDL_CreateRGBSurfaceWithFormat(0,            // flags
                                     FONT_WIDTH,   // width
                                     FONT_HEIGHT,  // height
                                     32,           // depth (bits per pixel)
                                     SDL_PIXELFORMAT_RGBA32  // pixel format
      );

  if (!surface) {
    return NULL;
  }

  // Map white and transparent colors
  Uint32 white = SDL_MapRGBA(surface->format, 255, 255, 255, 255);
  Uint32 transparent = SDL_MapRGBA(surface->format, 0, 0, 0, 0);

  // Lock surface if necessary
  if (SDL_MUSTLOCK(surface)) {
    SDL_LockSurface(surface);
  }

  // Fill surface pixels based on font_bitmap
  Uint32 *pixels = (Uint32 *)surface->pixels;
  for (int y = 0; y < FONT_HEIGHT; ++y) {
    for (int x = 0; x < FONT_WIDTH; ++x) {
      pixels[y * FONT_WIDTH + x] = font_bitmap[y][x] ? white : transparent;
    }
  }

  if (SDL_MUSTLOCK(surface)) {
    SDL_UnlockSurface(surface);
  }

  // Create texture from surface
  SDL_Texture *texture = SDL_CreateTextureFromSurface(renderer, surface);
  if (!texture) {
    SDL_FreeSurface(surface);
    return NULL;
  }

  // Set texture scale mode to nearest (pixelated)
  SDL_SetTextureScaleMode(texture, SDL_ScaleModeNearest);

  SDL_FreeSurface(surface);

  return texture;
}

void draw_char(SDL_Renderer *renderer, SDL_Texture *font_tex,
               unsigned char character, int cx, int cy, int scale) {
  SDL_Point cell = {(character - 32) % (FONT_WIDTH / GLYPH_WIDTH),
                    (character - 32) / (FONT_WIDTH / GLYPH_WIDTH)};
  const SDL_Rect srcrect = {cell.x * GLYPH_WIDTH, cell.y * GLYPH_HEIGHT,
                            GLYPH_WIDTH, GLYPH_HEIGHT};
  const SDL_Rect dstrect = {cx, cy, GLYPH_WIDTH * scale, GLYPH_HEIGHT * scale};
  SDL_RenderCopy(renderer, font_tex, &srcrect, &dstrect);
}

void draw_text(SDL_Renderer *renderer, SDL_Texture *font_tex, const char *text,
               int x, int y, int scale) {
  for (size_t i = 0; text[i] != '\0'; i++) {
    draw_char(renderer, font_tex, text[i], x + i * GLYPH_WIDTH * scale, y,
              scale);
  }
}
